package eu.unicredit.exception;

public class CheckTopicFailureException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3560226953001697754L;

	public CheckTopicFailureException() {
		super();
	}

	public CheckTopicFailureException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CheckTopicFailureException(String message, Throwable cause) {
		super(message, cause);
	}

	public CheckTopicFailureException(String message) {
		super(message);
	}

	public CheckTopicFailureException(Throwable cause) {
		super(cause);
	}

	
	
	
	
}
